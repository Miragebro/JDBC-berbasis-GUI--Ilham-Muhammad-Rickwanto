    private void ButtonCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonCariActionPerformed
        // TODO add your handling code here:
        cari = Integer.parseInt(JOptionPane.showInputDialog("Masukkan ID Buku"));
        
        PerpustakaanImplement pimp = new PerpustakaanImplement();
        Buku buku = pimp.get(cari);
        
        txtJudul.setText(buku.getJudul());
        txtTahun.setText(buku.getTahun());
        txtPenulis.setText(buku.getPenulis());
    }//GEN-LAST:event_ButtonCariActionPerformed

