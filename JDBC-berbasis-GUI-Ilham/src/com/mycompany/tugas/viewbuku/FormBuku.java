    public void displayBuku() {
        PerpustakaanImplement pimp = new PerpustakaanImplement();
        List<Buku> list = pimp.list();
        DefaultTableModel DFT = (DefaultTableModel) tabelBuku.getModel();
        DFT.setRowCount(0);
        for (Buku b : list) {
            int id = b.getId();
            String judul = b.getJudul();
            String tahun = b.getTahun();
            String penulis = b.getPenulis();
            DFT.addRow(new Object[] {id, judul, tahun, penulis});
        }
    }
    
    private void ButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonSimpanActionPerformed
        // TODO add your handling code here:
        Buku buku = new Buku();
        String judul = txtJudul.getText();
        String tahun = txtTahun.getText();
        String penulis = txtPenulis.getText();
        
        buku.setJudul(judul);
        buku.setTahun(tahun);
        buku.setPenulis(penulis);
        
        PerpustakaanImplement pimp = new PerpustakaanImplement();
        pimp.save(buku);
        
        displayBuku();
        
        txtJudul.setText("");
        txtTahun.setText("");
        txtPenulis.setText("");
        txtJudul.requestFocus();
    }//GEN-LAST:event_ButtonSimpanActionPerformed

    private void ButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonUpdateActionPerformed
        // TODO add your handling code here:
        Buku buku = new Buku();
        String judul = txtJudul.getText();
        String tahun = txtTahun.getText();
        String penulis = txtPenulis.getText();
        
        buku.setJudul(judul);
        buku.setTahun(tahun);
        buku.setPenulis(penulis);
        buku.setId(cari);
        
        PerpustakaanImplement pimp = new PerpustakaanImplement();
        pimp.update(buku);
        
        displayBuku();
        
        txtJudul.setText("");
        txtTahun.setText("");
        txtPenulis.setText("");
        txtJudul.requestFocus();                     
    }//GEN-LAST:event_ButtonUpdateActionPerformed

